    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        String id = jTextField1.getText();
        String name = jTextField2.getText();
        String fromtime = jTextField3.getText();
        String totime = jTextField4.getText();
        String date = String.valueOf(jComboBox2.getSelectedItem());
        String subject = String.valueOf(jComboBox1.getSelectedItem());
        String hall = String.valueOf(jComboBox3.getSelectedItem());
        String fees = jTextField6.getText();

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `class` WHERE `class_no`='" + id + "' ");
            if (resultSet.next()) {
                JOptionPane.showMessageDialog(this, "Class Number is already exist", "Class Registration", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Something Went Wrong..", "Error", JOptionPane.WARNING_MESSAGE);
        }

        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter class id", "Class Registration", JOptionPane.INFORMATION_MESSAGE);
        } else if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter class name", "Class Registration", JOptionPane.INFORMATION_MESSAGE);
        } else if (totime.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter time", "Class Registration", JOptionPane.INFORMATION_MESSAGE);
        } else if (fromtime.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter time", "Class Registration", JOptionPane.INFORMATION_MESSAGE);
        } else if (date.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a date", "Class Registration", JOptionPane.INFORMATION_MESSAGE);

        } else if (subject.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a subject", "Class Registration", JOptionPane.INFORMATION_MESSAGE);
        } else if (hall.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a class hall", "Class Registration", JOptionPane.INFORMATION_MESSAGE);

        } else if (fees.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter amount of class fees", "Class Registration", JOptionPane.INFORMATION_MESSAGE);

        } else {
            int subjectID = SubjectMap.get(subject);
            int dateID = DateMap.get(date);
            int hallID = HallMap.get(hall);

            try {

                MySQL.execute("INSERT INTO `class` (`class_no`,`name`,`time_from`,`time_to`,`sub_no`,`date_id`,`fees`,`hall_id`) VALUES "
                        + "('" + id + "','" + name + "','" + fromtime + "','" + totime + "','" + subjectID + "','" + dateID + "','" + fees + "','" + hallID + "')");

                final ImageIcon icon = new ImageIcon("src/img/correct.png");
                JOptionPane.showMessageDialog(this, "Class Added the System", "Class Registration", JOptionPane.INFORMATION_MESSAGE, icon);

                reset();
                loadClass();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Something Went Wrong..", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }


    }//GEN-LAST:event_jButton8ActionPerformed

