    private void jTextField5KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField5KeyReleased
        String clzId = jTextField5.getText();

        if (clzId.length() == 5) {
            try {

                ResultSet resultSet = MySQL.execute("SELECT * FROM `class` INNER JOIN "
                        + "`subject` ON `class`.`sub_no` =`subject`.`sub_no` "
                        + "INNER JOIN `hall` ON `hall`.`id`=`class`.`hall_id` "
                        + " INNER JOIN `date` ON `class`.`date_id` = `date`.`id` WHERE `class_no` ='" + clzId + "' ");

                DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
                model1.setRowCount(0);

                while (resultSet.next()) {
                    Vector<String> v = new Vector();
                    v.add(resultSet.getString("class_no"));
                    v.add(resultSet.getString("name"));
                    v.add(resultSet.getString("subject.name"));
                    v.add(resultSet.getString("hall.name"));
                    v.add(resultSet.getString("date.name"));
                    v.add(resultSet.getString("time_from"));
                    v.add(resultSet.getString("time_to"));
                    v.add(resultSet.getString("fees"));

                    model1.addRow(v);
                    jTable1.setModel(model1);

                }

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Something Went Wrong..", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jTextField5KeyReleased

